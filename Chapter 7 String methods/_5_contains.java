
public class _5_contains {
    public static void main(String[] args) {
        // syntax 
        //  contains(CharSequence chars)

        /* The contains() method checks whether a string contains a sequence of characters.
        
        Returns true if the characters exist and false if not. */

        String myStr = "Rakesh";
        // tip     Make sure u add " "  this
        //         ' '  is used for characters while for strings we use " " double quotes
        System.out.println(myStr.contains("R"));
    }
}
