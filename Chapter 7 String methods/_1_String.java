
// imp      The charAt() method returns the character at the specified index in a string.
//          The index of the first character is 0, the second character is 1, and so on.
//          It takes integer value and returns a character 
public class _1_String {
    public static void main(String[] args) {
        String s = "Rakesh Singh";
        System.out.println(s.charAt(6)); // outputs space
        System.out.println(s.charAt(7)); // outputs space


        String name = "rakesh";
        //      Outputs r
        System.out.println(name.charAt(0));


    }
}
