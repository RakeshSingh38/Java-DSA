
public class _7_endsWith {
    public static void main(String[] args) {
        //Find out if the string ends with the specified characters:
        String myString = "Rakesh";
        System.out.println("Is the Ends with h ? " + myString.endsWith("h"));

        //startsWith() method to check whether a string starts with the specified character(s).
        String isStarting = "Singh";
        System.out.println("Is the String starts with s ? " + isStarting.startsWith("S"));

    }

}
